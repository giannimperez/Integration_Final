/**
 * This class explains the basics of inheritance. The example of inheritance can
 * be found in the Car class.
 * 
 * @author Gianni Perez
 **/
public class Inheritance {

	public static void main() {
		System.out.println("_____Inheritance_____");
		System.out.println("Inheritance is a tool used to derive a new class from a parent class.");
		System.out.println("This new class will inherit the same properties and methods of");
		System.out.println("the parent class.");
		System.out.println();
		System.out.println("This is particularly useful when you already have a");
		System.out.println("very generic class, such as \"car\" and you would");
		System.out.println("like to create classes for \"Ford, Chevy, and Toyota\"");
		System.out.println();
		System.out.println("Your headers would look something like this:");
		System.out.println();
		System.out.println("class Car");
		System.out.println("class Ford extends Car");
		System.out.println("class Chevy extends Car");
		System.out.println("class Toyota extends Car");
		System.out.println();

	}

}