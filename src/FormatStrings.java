/**
 * This class prints simple explanations of the tools to format Strings.
 * 
 * @author Gianni Perez
 **/
public class FormatStrings {

	public static void main() {

		System.out.println();
		System.out.println("_____Formatting Strings Using Escape Sequences_____");
		System.out.println(
				"When writing long strings, it is sometimes useful to utilize escape sequences to format your String.");
		System.out.println("Escape Sequences:");
		System.out.println("\\t : Insert a tab in the text.");
		System.out.println("\\b : Insert a backspace in the text.");
		System.out.println("\\n : Insert a newline in the text.");
		System.out.println("\\r : Insert a carriage return in the text.");
		System.out.println("\\f : Insert a formfeed in the text.");
		System.out.println("\\' : Insert a single quote character in the text.");
		System.out.println("\\\" : Insert a \" in the text.");
		System.out.println("\\\\ : Insert a \\ in the text.");
		System.out.println();
	}

}